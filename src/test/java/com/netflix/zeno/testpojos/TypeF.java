package com.netflix.zeno.testpojos;

public class TypeF {

    private final int value;

    public TypeF(int value) {
        this.value = value;
    }

    public int getValue() {
        return value;
    }

    @Override
    public int hashCode() {
        return value;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj)
            return true;
        if (obj == null)
            return false;
        if (getClass() != obj.getClass())
            return false;
        TypeF other = (TypeF) obj;
        if (value != other.value)
            return false;
        return true;
    }



}
